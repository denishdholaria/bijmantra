def _run_migration_upgrade(connection, migration) -> None:
    context = MigrationContext.configure(connection)
    operations = Operations(context)
    previous_proxy = getattr(alembic_op, "_proxy", None)
    alembic_op._proxy = operations
    try:
        migration.upgrade()
    finally:
        if previous_proxy is None:
            if hasattr(alembic_op, "_proxy"):
                del alembic_op._proxy
        else:
            alembic_op._proxy = previous_proxy


@pytest.fixture(scope="module")
def postgres_engine():
    dsn = os.getenv(POSTGRES_DSN_ENV)
    if not dsn:
        pytest.skip(f"set {POSTGRES_DSN_ENV} to run Postgres RLS integration tests")

    engine = create_engine(dsn, future=True)
    with engine.connect() as connection:
        role = connection.execute(
            text(
                """
                SELECT rolsuper, rolbypassrls
                FROM pg_roles
                WHERE rolname = current_user
                """
            )
        ).one()
        if role.rolsuper or role.rolbypassrls:
            pytest.skip("Postgres RLS proof requires a non-superuser role without BYPASSRLS")

    try:
        yield engine
    finally:
        engine.dispose()


