def test_upgrade_backfills_organization_ids_and_enables_veena_policies(postgres_engine, veena_rls_schema):
    tables = [
        "research_projects",
        "paper_experiment_links",
        "writing_sessions",
        "committee_members",
        "committee_meetings",
        "feedback_items",
        "veena_memories_v2",
        "veena_user_contexts_v2",
        "veena_audit_logs",
        "veena_reasoning_traces_v2",
    ]

    with postgres_engine.begin() as connection:
        _set_search_path(connection, veena_rls_schema)
        _set_rls_context(connection, organization_id=0, user_id=10)

        null_counts = {
            table_name: connection.execute(
                text(f"SELECT COUNT(*) FROM {table_name} WHERE organization_id IS NULL")
            ).scalar_one()
            for table_name in tables
        }

        owner_trace_org = connection.execute(
            text(
                "SELECT organization_id FROM veena_reasoning_traces_v2 WHERE session_id = 'session-owner'"
            )
        ).scalar_one()
        owner_audit_org = connection.execute(
            text("SELECT organization_id FROM veena_audit_logs WHERE session_id = 'session-owner'")
        ).scalar_one()
        owner_memory_org = connection.execute(
            text("SELECT organization_id FROM veena_memories_v2 WHERE user_id = 10")
        ).scalar_one()
        owner_project_org = connection.execute(
            text("SELECT organization_id FROM research_projects WHERE id = 'proj-owner'")
        ).scalar_one()
        force_rls = connection.execute(
            text(
                """
                SELECT relrowsecurity, relforcerowsecurity
                FROM pg_class
                WHERE oid = to_regclass(:table_name)
                """
            ),
            {"table_name": f'{veena_rls_schema}.veena_reasoning_traces_v2'},
        ).one()

    assert all(count == 0 for count in null_counts.values())
    assert owner_trace_org == 1
    assert owner_audit_org == 1
    assert owner_memory_org == 1
    assert owner_project_org == 1
    assert force_rls.relrowsecurity is True
    assert force_rls.relforcerowsecurity is True


def test_reasoning_trace_reads_are_scoped_by_user_and_org(postgres_engine, veena_rls_schema):
    with postgres_engine.begin() as connection:
        _set_search_path(connection, veena_rls_schema)
        _set_rls_context(connection, organization_id=1, user_id=10)

        owner_trace_count = connection.execute(
            text("SELECT COUNT(*) FROM veena_reasoning_traces_v2 WHERE session_id = 'session-owner'")
        ).scalar_one()
        owner_audit_count = connection.execute(
            text("SELECT COUNT(*) FROM veena_audit_logs WHERE session_id = 'session-owner'")
        ).scalar_one()

    assert owner_trace_count == 1
    assert owner_audit_count == 1

    with postgres_engine.begin() as connection:
        _set_search_path(connection, veena_rls_schema)
        _set_rls_context(connection, organization_id=1, user_id=11)

        peer_trace_count = connection.execute(
            text("SELECT COUNT(*) FROM veena_reasoning_traces_v2 WHERE session_id = 'session-owner'")
        ).scalar_one()
        peer_audit_count = connection.execute(
            text("SELECT COUNT(*) FROM veena_audit_logs WHERE session_id = 'session-owner'")
        ).scalar_one()

    assert peer_trace_count == 0
    assert peer_audit_count == 0

    with postgres_engine.begin() as connection:
        _set_search_path(connection, veena_rls_schema)
        _set_rls_context(connection, organization_id=2, user_id=20)

        cross_org_trace_count = connection.execute(
            text("SELECT COUNT(*) FROM veena_reasoning_traces_v2 WHERE session_id = 'session-owner'")
        ).scalar_one()
        own_org_trace_count = connection.execute(
            text("SELECT COUNT(*) FROM veena_reasoning_traces_v2 WHERE session_id = 'session-other-org'")
        ).scalar_one()

    assert cross_org_trace_count == 0
    assert own_org_trace_count == 1


