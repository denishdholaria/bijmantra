def test_reasoning_trace_insert_requires_matching_audit_log(postgres_engine, veena_rls_schema):
    with postgres_engine.begin() as connection:
        _set_search_path(connection, veena_rls_schema)
        _set_rls_context(connection, organization_id=1, user_id=10)

        connection.execute(
            text(
                """
                INSERT INTO veena_reasoning_traces_v2 (organization_id, session_id, step_id, thought, tool_used, outcome)
                VALUES (1, 'session-owner', 'retrieve', 'Fetch relevant memory', 'pgvector', 'context_hits=1')
                """
            )
        )

        owner_insert_count = connection.execute(
            text("SELECT COUNT(*) FROM veena_reasoning_traces_v2 WHERE session_id = 'session-owner'")
        ).scalar_one()

    assert owner_insert_count == 2

    with pytest.raises(exc.DBAPIError) as peer_insert_error:
        with postgres_engine.begin() as connection:
            _set_search_path(connection, veena_rls_schema)
            _set_rls_context(connection, organization_id=1, user_id=11)
            connection.execute(
                text(
                    """
                    INSERT INTO veena_reasoning_traces_v2 (organization_id, session_id, step_id, thought, tool_used, outcome)
                    VALUES (1, 'session-owner', 'peer-write', 'Peer write attempt', 'pgvector', 'denied')
                    """
                )
            )
    _assert_rls_violation(peer_insert_error.value)

    with pytest.raises(exc.DBAPIError) as no_context_insert_error:
        with postgres_engine.begin() as connection:
            _set_search_path(connection, veena_rls_schema)
            connection.execute(
                text(
                    """
                    INSERT INTO veena_reasoning_traces_v2 (organization_id, session_id, step_id, thought, tool_used, outcome)
                    VALUES (1, 'session-owner', 'no-context-write', 'No context write attempt', 'pgvector', 'denied')
                    """
                )
            )
    _assert_rls_violation(no_context_insert_error.value)


