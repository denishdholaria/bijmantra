def _load_migration_module():
    module_path = (
        Path(__file__).resolve().parents[2]
        / "alembic/versions/20260313_0200_adr006_devguru_veena_ownership_rls.py"
    )
    spec = importlib.util.spec_from_file_location("adr006_veena_rls_migration", module_path)
    assert spec is not None
    assert spec.loader is not None
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    return module


def _set_search_path(connection, schema_name: str) -> None:
    connection.execute(text(f'SET search_path TO "{schema_name}", public'))


def _set_rls_context(connection, organization_id: int, user_id: int) -> None:
    connection.execute(
        text("SELECT set_config('app.current_organization_id', :value, true)"),
        {"value": str(organization_id)},
    )
    connection.execute(
        text("SELECT set_config('app.current_user_id', :value, true)"),
        {"value": str(user_id)},
    )

    current_org = connection.execute(
        text("SELECT current_setting('app.current_organization_id', true)")
    ).scalar_one()
    current_user = connection.execute(
        text("SELECT current_setting('app.current_user_id', true)")
    ).scalar_one()

    assert current_org == str(organization_id)
    assert current_user == str(user_id)


